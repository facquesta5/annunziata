    <!-- Qualidade do Ar -->
    <h3><a id="qualidade_do_ar">Qualidade do Ar</a></h3>
    <p>A L.Annunziata é pioneira no segmento de manutenção e monitoramento da qualidade do ar 
        interior de Edifícios Climatizados.</p>
    <p>Hoje a grande parte dos edifícios são herméticos para proporcionar um maior bem estar ao usuário, isolando-o de ruídos e alterações térmicas.</p>
<p>Estima-se que 80 a 90% do nosso dia passamos em locais fechados, seja no trabalho, lazer ou em 
   casa. Daí a importância de uma boa qualidade do ar interior para uma vida saudável.</p>
<p>Mas, se o sistema de climatização de um edifício não for bem cuidado por profissionais capacitados, 
    torna-se uma fonte contaminadora, desencadeando uma série de doenças alérgicas, respiratórias ou 
    infecciosas, causadas por bactérias que se formam no sistema, afetando produtividade, saúde e 
    qualidade de vida.</p>
<p>As variáveis que interferem na qualidade do ar podem ser químicas, microbiológicas ou físicas.</p>
<p>Produtos químicos diversos presentes no ambiente, fungos, bactérias, materiais particulados e fibras, são os 
    principais responsáveis pela contaminação do ambiente. Na ausência de uma higienização permanente e 
    filtragem eficiente dos condicionadores, dutos de distribuição do ar, que aglutinam fungos e bactérias, 
    completa-se o círculo vicioso da contaminação. Os poluentes que já vivem no ambiente fechado funcionam 
    como um retroalimentador do sistema, pois retornam a casa de máquinas prejudicando continuamente a 
    qualidade do ar interior.</p>
    
